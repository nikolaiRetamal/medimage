package cnam.medimage.bean;

public class AccueilForm {

	private String usage; 
	private String usageConnu;
	private String metadatas;
	private String chosenTagsValue;
	
	public String getUsage() {
		return usage;
	}
	public void setUsage(String usage) {
		this.usage = usage;
	}
	public String getUsageConnu() {
		return usageConnu;
	}
	public void setUsageConnu(String usageConnu) {
		this.usageConnu = usageConnu;
	}
	public String getMetadatas() {
		return metadatas;
	}
	public void setMetadatas(String metadatas) {
		this.metadatas = metadatas;
	}
	public String getChosenTagsValue() {
		return chosenTagsValue;
	}
	public void setChosenTagsValue(String chosenTagsValue) {
		this.chosenTagsValue = chosenTagsValue;
	}
}
